private double hitungSkorDiagnosis() {
    try {
        double totalSkor = 0;

        // Pertanyaan 1 hingga 12 (gunakan loop)
        for (int i = 1; i <= 12; i++) {
            JComboBox<String> currentQuestion = (JComboBox<String>) getClass().getDeclaredField("ques" + i).get(this);
            Object selectedItem = currentQuestion.getSelectedItem();
            System.out.println("Question " + i + ": " + selectedItem);
            
            // Ganti bagian ini
            totalSkor += (selectedItem != null && selectedItem.toString().equals("Ya")) ? hitungBobotSkor(i) : 0;
        }

        // Bulatkan total skor ke dua desimal
        totalSkor = Math.round(totalSkor * 100.0) / 100.0;

        System.out.println("Total Skor: " + totalSkor);

        return hitungRataRataSkor(totalSkor);
    } catch (NoSuchFieldException | IllegalArgumentException | IllegalAccessException e) {
        e.printStackTrace();
        return 0; // atau nilai lain yang sesuai
    }
}


  private double hitungRataRataSkor(double totalSkor) {
    System.out.println("Total Skor Sebelum Pembulatan: " + totalSkor);
    totalSkor = Math.round(totalSkor * 100.0) / 100.0;
    System.out.println("Total Skor Setelah Pembulatan: " + totalSkor);
    double rataRataSkor = totalSkor / 12.0;
    System.out.println("Rata-rata Skor: " + rataRataSkor);
    return rataRataSkor;
}




    private double hitungBobotSkor(int questionNumber) {
        switch (questionNumber) {
        case 1: return 0.20;
        case 2: return 0.15;
        case 3: return 0.85;
        case 4: return 0.60;
        case 5: return 0.50;
        case 6: return 0.35;
        case 7: return 0.30;
        case 8: return 0.45;
        case 9: return 0.40;
        case 10: return 0.60;
        case 11: return 0.80;
        case 12: return 0.80;
        default: return 0;
    }
}



private void tampilkanHasilDiagnosis(double totalSkor) {
    String namaPasien = txt_input_namapasien.getText();
    double rataRataSkor = hitungRataRataSkor(totalSkor);
    String diagnosisMessage = getDiagnosisMessageFromDatabase(rataRataSkor);

    String htmlMessage = "<html>"
            + "<b>Hasil Diagnosis untuk Pasien:</b><br>"
            + "<b>Nama Pasien:</b> " + namaPasien + "<br><br>"
            + "<b>Diagnosis:</b><br>" + diagnosisMessage
            + "<b>Rata-rata skor diagnosis:</b><br>" + rataRataSkor;

    JOptionPane.showMessageDialog(this, htmlMessage, "Hasil Diagnosis", JOptionPane.INFORMATION_MESSAGE);
}

private String getDiagnosisMessageFromDatabase(double skor) {
    String diagnosisMessage = "Data tidak ditemukan.";
    String query = "SELECT Keterangan, Saran FROM cf WHERE Skor_diagnosis = ?";

    try (PreparedStatement preparedStatement = koneksi.prepareStatement(query)) {
        preparedStatement.setDouble(1, skor);
        try (ResultSet resultSet = preparedStatement.executeQuery()) {
            if (resultSet.next()) {
                String keterangan = resultSet.getString("Keterangan");
                String saran = resultSet.getString("Saran");
                diagnosisMessage = "<b>Keterangan:</b><br>" + keterangan + "<br><br><b>Saran:</b><br>" + saran;
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
        diagnosisMessage = "Error dalam mengambil data dari database: " + e.getMessage();
    }
    return diagnosisMessage;
}

