private String hasilDiagnosisBerbasisSkor(double skor) {
    if (skor >= 0.00 && skor <= 0.19) {
        return "Tidak Stress";
    } else if (skor >= 0.20 && skor <= 0.29) {
        return "Stress Ringan";
    } else if (skor >= 0.30 && skor <= 0.39) {
        return "Stress Sedang";
    } else {
        return "Stress Berat";
    }
}
    
private void simpanDataDiagnosa(double skor) {
    String query = "INSERT INTO history (UserID, skor_diagnosis, Keterangan, Saran, Penyebab) VALUES (?, ?, ?, ?, ?)";
    
    try (PreparedStatement preparedStatement = koneksi.prepareStatement(query)) {
        preparedStatement.setInt(1, 1);  // Ganti 1 dengan UserID yang sesuai
        preparedStatement.setDouble(2, Math.round(skor * 100.0) / 100.0);
        preparedStatement.setString(3, "Keterangan Contoh");  // Ganti dengan nilai Keterangan yang sesuai
        preparedStatement.setString(4, "Saran Contoh");  // Ganti dengan nilai Saran yang sesuai
        preparedStatement.setString(5, "Penyebab Contoh");  // Ganti dengan nilai Penyebab yang sesuai

        preparedStatement.executeUpdate();
        System.out.println("Data berhasil disimpan ke database.");
    } catch (SQLException e) {
        System.err.println("Gagal menyimpan data ke database. Error: " + e.getMessage());
    }
}



